'use strict';

import StoreHelper from '../../lib/StoreHelper';

export const LOCKS_ACTION_TYPES = {
    LOCKS_FETCH: 'LOCKS_FETCH',
    LOCKS_FETCH_SUCCESS: 'LOCKS_FETCH_SUCCESS',
    LOCKS_FETCH_FAIL: 'LOCKS_FETCH_FAIL',
    LOCKS_CLEAR: 'LOCKS_CLEAR',
    LOCK_BRANCHES_ASSIGNMENTS_FETCH: 'LOCK_BRANCHES_ASSIGNMENTS_FETCH',
    LOCK_BRANCHES_ASSIGNMENTS_FETCH_SUCCESS: 'LOCK_BRANCHES_ASSIGNMENTS_FETCH_SUCCESS',
    LOCK_BRANCHES_ASSIGNMENTS_FETCH_FAIL: 'LOCK_BRANCHES_ASSIGNMENTS_FETCH_FAIL',
    LOCK_BRANCHES_ASSIGNMENTS_CLEAR: 'LOCK_BRANCHES_ASSIGNMENTS_CLEAR',
    LOCK_BRANCHES_ASSIGNMENTS_SAVE: 'LOCK_BRANCHES_ASSIGNMENTS_SAVE',
    LOCK_BRANCHES_ASSIGNMENTS_SAVE_SUCCESS: 'LOCK_BRANCHES_ASSIGNMENTS_SAVE_SUCCESS',
    LOCK_BRANCHES_ASSIGNMENTS_SAVE_FAIL: 'LOCK_BRANCHES_ASSIGNMENTS_SAVE_FAIL'
}
export const fetchLocks = () => StoreHelper.createAction(LOCKS_ACTION_TYPES.LOCKS_FETCH);

export const clearLocks = () => StoreHelper.createAction(LOCKS_ACTION_TYPES.LOCKS_CLEAR);

export const fetchLockBranchesAssignments = (lockId) => StoreHelper.createAction(LOCKS_ACTION_TYPES.LOCK_BRANCHES_ASSIGNMENTS_FETCH, lockId);

export const saveLockBranchesAssignments = (lockId, assignments) => StoreHelper.createAction(LOCKS_ACTION_TYPES.LOCK_BRANCHES_ASSIGNMENTS_SAVE, { lockId, assignments });

export const clearLockBranchesAssignments = () => StoreHelper.createAction(LOCKS_ACTION_TYPES.LOCK_BRANCHES_ASSIGNMENTS_CLEAR);