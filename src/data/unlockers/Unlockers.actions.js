'use strict';

import StoreHelper from '../../lib/StoreHelper';

export const UNLOCKERS_ACTION_TYPES = {
    UNLOCKERS_FETCH: 'UNLOCKERS_FETCH',
    UNLOCKERS_FETCH_SUCCESS: 'UNLOCKERS_FETCH_SUCCESS',
    UNLOCKERS_FETCH_FAIL: 'UNLOCKERS_FETCH_FAIL',
    UNLOCKERS_CLEAR: 'UNLOCKERS_CLEAR',
    UNLOCKER_EMPLOYEES_ASSIGNMENTS_FETCH: 'UNLOCKER_EMPLOYEES_ASSIGNMENTS_FETCH',
    UNLOCKER_EMPLOYEES_ASSIGNMENTS_FETCH_SUCCESS: 'UNLOCKER_EMPLOYEES_ASSIGNMENTS_FETCH_SUCCESS',
    UNLOCKER_EMPLOYEES_ASSIGNMENTS_FETCH_FAIL: 'UNLOCKER_EMPLOYEES_ASSIGNMENTS_FETCH_FAIL',
    UNLOCKER_EMPLOYEES_ASSIGNMENTS_CLEAR: 'UNLOCKER_EMPLOYEES_ASSIGNMENTS_CLEAR',
    UNLOCKER_EMPLOYEES_ASSIGNMENTS_SAVE: 'UNLOCKER_EMPLOYEES_ASSIGNMENTS_SAVE',
    UNLOCKER_EMPLOYEES_ASSIGNMENTS_SAVE_SUCCESS: 'UNLOCKER_EMPLOYEES_ASSIGNMENTS_SAVE_SUCCESS',
    UNLOCKER_EMPLOYEES_ASSIGNMENTS_SAVE_FAIL: 'UNLOCKER_EMPLOYEES_ASSIGNMENTS_SAVE_FAIL',
}

export const fetchUnlockers = () => StoreHelper.createAction(UNLOCKERS_ACTION_TYPES.UNLOCKERS_FETCH);

export const clearUnlockers = () => StoreHelper.createAction(UNLOCKERS_ACTION_TYPES.UNLOCKERS_CLEAR);

export const fetchUnlockerEmployeesAssignments = (type, id) => StoreHelper.createAction(UNLOCKERS_ACTION_TYPES.UNLOCKER_EMPLOYEES_ASSIGNMENTS_FETCH, { type, id });

export const saveUnlockerEmployeesAssignments = (type, id, assignments) => StoreHelper.createAction(UNLOCKERS_ACTION_TYPES.UNLOCKER_EMPLOYEES_ASSIGNMENTS_SAVE, { type, id, assignments });

export const clearUnlockerEmployeesAssignments = () => StoreHelper.createAction(UNLOCKERS_ACTION_TYPES.UNLOCKER_EMPLOYEES_ASSIGNMENTS_CLEAR);
